import React from 'react'

export interface IconProps {
  /**
   * 组件默认的选择器类
   */
  prefixCls?: string
  /**
   * 组件的语义化 Role 属性
   */
  role?: string
  /**
   * 组件的注入选择器类
   */
  className?: string
  /**
   * 组件的注入样式
   */
  style?: React.CSSProperties
  /**
   * 点击事件
   */
  onClick?: (e: React.MouseEvent<SVGSVGElement, MouseEvent>) => void
}
